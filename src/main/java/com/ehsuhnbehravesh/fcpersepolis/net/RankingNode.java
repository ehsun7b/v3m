package com.ehsuhnbehravesh.fcpersepolis.net;

/**
 * 
 * @author ehsun.behravesh
 */
public class RankingNode {

	private String team;
	private String matches;
	private String wins;
	private String draws;
	private String looses;
	private String scoredGoals;
	private String receivedGoals;
	private String differentGoals;
	private String points;

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public String getMatches() {
		return matches;
	}

	public void setMatches(String matches) {
		this.matches = matches;
	}

	public String getWins() {
		return wins;
	}

	public void setWins(String wins) {
		this.wins = wins;
	}

	public String getDraws() {
		return draws;
	}

	public void setDraws(String draws) {
		this.draws = draws;
	}

	public String getLooses() {
		return looses;
	}

	public void setLooses(String looses) {
		this.looses = looses;
	}

	public String getScoredGoals() {
		return scoredGoals;
	}

	public void setScoredGoals(String scoredGoals) {
		this.scoredGoals = scoredGoals;
	}

	public String getReceivedGoals() {
		return receivedGoals;
	}

	public void setReceivedGoals(String receivedGoals) {
		this.receivedGoals = receivedGoals;
	}

	public String getDifferentGoals() {
		return differentGoals;
	}

	public void setDifferentGoals(String differentGoals) {
		this.differentGoals = differentGoals;
	}

	public String getPoints() {
		return points;
	}

	public void setPoints(String points) {
		this.points = points;
	}
}